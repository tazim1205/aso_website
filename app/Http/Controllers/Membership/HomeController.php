        $adminAds = AdminAds::where('status', '1')
            ->whereDate('starting', '<', Carbon::today()->addDays(1))
            ->whereDate('ending', '>', Carbon::today()->addDays(-1))
            ->get();
        $adminNotice = AdminNotice::orderBy('id', 'desc')
            ->take(1)
            ->get();
